<?php

// Создание строковой переменной s1 со значением "Я люблю Беларусь"
$s1 = "Я люблю Беларусь";
// Создание строковой переменной s2 со значением "Я уЧУСЬ В Политехническом колледже"
$s2 = "Я уЧУСЬ В Политехническом колледже";

// 1. Определение длины строки s2
$length_s2 = mb_strlen($s2, 'UTF-8');
echo "Длина строки s2: " . $length_s2 . "<br>";

// 2. Выделение п-ого символа в строке s1 и определение ASCII-кода этого символа (например, п = 5)
$position = 5;
$char_at_position = mb_substr($s1, $position - 1, 1, 'UTF-8');
$ascii_code = ord($char_at_position);
echo "П-ый символ в строке s1: " . $char_at_position . ", ASCII-код: " . $ascii_code . "<br>";

// 3. Замена в строке s1 слова "Беларусь" на слово "Гродно"
$s1_modified = str_replace("Беларусь", "Гродно", $s1);
echo "Строка s1 после замены: " . $s1_modified . "<br>";

