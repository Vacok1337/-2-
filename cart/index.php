// Сформировать массив из 5 целых элементов
$array = [3, -5, 7, -2, 4];

// Инициализировать переменную для хранения суммы положительных элементов
$sum_of_positives = 0;

// Пройти по каждому элементу массива и добавить положительные элементы к сумме
foreach ($array as $element) {
    if ($element > 0) {
        $sum_of_positives += $element;
    }
}

// Вывести сумму положительных элементов на экран
echo "Сумма положительных элементов в массиве: " . $sum_of_positives;

